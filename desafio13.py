# Crie um loop (não pode ser o for loop) para imprimir os números de 1 a 10 na tela.  

# Criamos nossa variavel
i = 1 

# Colocamos a condição while;
# Printamos o número
# Acrescentamos o incremento

while i <=10:
    print(i)
    i += 1

# while = enquanto i for menor ou igual a 10, printe ele e acrescente mais 1. Quando i for igual a 10, o while para de executar.