'''Use a lista de frutas do exercício anterior. Use um "for loop" para percorrer a lista 
e imprima cada fruta precedida pelo texto: Eu gosto de:...'''

frutas = ['Maçã', 'Morango', 'Manga', 'Uva', 'Abacaxi']

# Criando o loop
for fruta in frutas:
    print(f'Eu gosto de {fruta}.')

# ou podemos fazer assim
print('Eu gosto de ' + fruta)
